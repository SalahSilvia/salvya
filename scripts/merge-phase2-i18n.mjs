function flat(obj, p = "") {
  const o = {};
  for (const [k, v] of Object.entries(obj)) {
    const key = p ? `${p}.${k}` : k;
    if (v && typeof v === "object" && !Array.isArray(v)) Object.assign(o, flat(v, key));
    else o[key] = 1;
  }
  return o;
}
